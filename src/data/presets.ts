export function getPresetsForDataset(dataset: DatasetKey): RolePreset[] {
  return ROLE_PRESETS.filter((preset) => preset.dataset === dataset)
}

export function getPresetById(
  dataset: DatasetKey,
  presetId: string,
): RolePreset | undefined {
  return ROLE_PRESETS.find(
    (preset) => preset.dataset === dataset && preset.id === presetId,
  )
}
